package chess.pieces;

import boardgame.Board;
import boardgame.Position;
import chess.ChessPiece;
import chess.Color;

public class Rook extends ChessPiece {

	public Rook(Board board, Color color) {
		super(board, color);
	}

	@Override
	public String toString() {
		return "R";
	}
	
	// Method para verificar se a posição da peça é possivel;
	@Override
	public boolean[][] possibleMoves() {
		boolean [][] mat = new boolean [getBoard().getRow()][getBoard().getColumns()];
		
		Position p = new Position(0, 0);
		
		//Marcação das casas possíveis ACIMA da peça como verdadeiro
		p.setValues(position.getRow() -1 , position.getColumn());
		while (getBoard().positionExists(p) && !getBoard().thereIsAPiace(p)) {
			mat[p.getRow()][p.getColumn()] = true;
			p.setRow(p.getRow() - 1);
		}
		if (getBoard().positionExists(p) && isThereOpponentPiece(p)) {
			mat[p.getRow()][p.getColumn()] = true;
		}
		
		//Marcação das casas possíveis ESQUERDA da peça como verdadeiro
		p.setValues(position.getRow() , position.getColumn() - 1);
		while (getBoard().positionExists(p) && !getBoard().thereIsAPiace(p)) {
			mat[p.getRow()][p.getColumn()] = true;
			p.setColumn(p.getColumn() - 1);
		}
		if (getBoard().positionExists(p) && isThereOpponentPiece(p)) {
			mat[p.getRow()][p.getColumn()] = true;
		}
		
		//Marcação das casas possíveis DIREITA da peça como verdadeiro
		p.setValues(position.getRow() , position.getColumn() + 1);
		while (getBoard().positionExists(p) && !getBoard().thereIsAPiace(p)) {
			mat[p.getRow()][p.getColumn()] = true;
			p.setColumn(p.getColumn() + 1);
		}
		if (getBoard().positionExists(p) && isThereOpponentPiece(p)) {
			mat[p.getRow()][p.getColumn()] = true;
		}
		
		//Marcação das casas possíveis BAIXO da peça como verdadeiro
		p.setValues(position.getRow() +1 , position.getColumn());
		while (getBoard().positionExists(p) && !getBoard().thereIsAPiace(p)) {
			mat[p.getRow()][p.getColumn()] = true;
			p.setRow(p.getRow() + 1);
		}
		if (getBoard().positionExists(p) && isThereOpponentPiece(p)) {
			mat[p.getRow()][p.getColumn()] = true;
		}
		
		return null;
	}
}
